package ec.edu.intsuperior.modelo;

/**
 *
 * @author USER
 */
public class Futbolista extends Persona{
    
    private String puesto;
    private double peso;
    private double altura;
    private int edad;

    public Futbolista() {
    }

    public Futbolista(String puesto, double peso, double altura, int edad) {
        this.puesto = puesto;
        this.peso = peso;
        this.altura = altura;
        this.edad = edad;
    }

    public String getPuesto() {
        return puesto;
    }

    public void setPuesto(String puesto) {
        this.puesto = puesto;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
